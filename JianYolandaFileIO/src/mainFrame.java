    private void resetButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_resetButtonActionPerformed

        String username = resetUsernameTF.getText();
        String email = resetEmailTF.getText();
        String newPwd = resetNewPwdTF.getText();
        String name = "";
        int curSize = usernames.size();
        boolean match = false;

        if (username.length() == 0 || email.length() == 0 || newPwd.length() == 0) {
            resetLabel.setText("Please fill out all the information above.");
        } else {

            for (int a = 0; a < curSize; a++) {
                if (username.equals(usernames.get(a)) && email.equals(emails.get(a))) {
                    name = names.get(a).toString();
                    boolean goodPwd = true;
                    match = true;
                    try {
                        goodPwd = comparePwd(newPwd);
                    } catch (FileNotFoundException ex) {

                    }
                    if (!goodPwd) {
                        resetLabel.setText("Please choose a stronger password.");
                    } else if (newPwd.length() < 4) {
                        resetLabel.setText("Please choose a longer password.");
                    } else {
                        String encryptedPwd = pwdEncryption(newPwd);
                        fileWrite(name, username, email, encryptedPwd);
                        resetLabel.setText("Password reset successful.");
                        resetUsernameTF.setText("");
                        resetEmailTF.setText("");
                        resetNewPwdTF.setText("");
                    }
                }
            }
            if (!match) {
                resetLabel.setText("Username and email do not match.");
                resetUsernameTF.setText("");
                resetEmailTF.setText("");
            }

        }


    }//GEN-LAST:event_resetButtonActionPerformed

