    public void fileRead(File usersInfo) throws FileNotFoundException {
        Scanner read = new Scanner(usersInfo);
        try {

            while (true) {
                String userInfo = read.nextLine();
                StringTokenizer st = new StringTokenizer(userInfo, ";", false);
                names.add(st.nextToken());
                usernames.add(st.nextToken());
                emails.add(st.nextToken());
                passwords.add(st.nextToken());
            }

        } catch (NoSuchElementException ex) {

        }
    }

    public boolean comparePwd(String password) throws FileNotFoundException {
        boolean goodPassword = true;
        Scanner comparePassword = new Scanner(badPassword);
        try {
            while (true) {

                String bad = comparePassword.nextLine();
                if (bad.equals(password)) {
                    goodPassword = false;
                }
            }
        } catch (NoSuchElementException ex) {

        }
        return goodPassword;

    }

    public void fileWrite(String n, String u, String e, String p) {
        names.add(n);
        usernames.add(u);
        emails.add(e);
        passwords.add(p);

        try {
            PrintWriter output = new PrintWriter(file);
            for (int x = 0; x <= names.size() - 1; x++) {
                output.println(names.get(x) + ";" + usernames.get(x) + ";" + emails.get(x) + ";" + passwords.get(x));
            }
            output.close();
        } catch (IOException ex) {
        }
    }

    public String pwdEncryption(String password) {
        String encrypted = "";
        try {
            MessageDigest md = MessageDigest.getInstance("MD5");
            md.update(password.getBytes());
            byte byteData[] = md.digest();
            for (int i = 0; i < byteData.length; i++) {
                encrypted += (Integer.toHexString((byteData[i] & 0xFF) | 0x100).substring(1, 3));
            }
        } catch (NoSuchAlgorithmException ex) {

        }
        return encrypted;
    }

