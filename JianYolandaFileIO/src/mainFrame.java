    private void regButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_regButtonActionPerformed

        String name = regNameTF.getText();
        String username = regUsernameTF.getText();
        String email = regEmailTF.getText();
        String password = regPwdTF.getText();
        boolean goodPwd = false;
        try {
            goodPwd = comparePwd(password);
        } catch (FileNotFoundException ex) {

        }

        if (name.length() == 0 || username.length() == 0 || email.length() == 0 || password.length() == 0) {
            jLabel8.setText("Please fill out all the information above.");
        } else if (!goodPwd) {
            jLabel8.setText("Please choose a stronger password.");
            regPwdTF.setText("");
        } else if (password.length() < 4) {
            jLabel8.setText("Please choose a longer password.");
            regPwdTF.setText("");
        } else if (email.indexOf('.') == -1 || email.indexOf('@') == -1) {
            jLabel8.setText("Please enter a valid email.");
            regEmailTF.setText("");
        } else {
            String encryptedPwd = pwdEncryption(password);
            fileWrite(name, username, email, encryptedPwd);
            jLabel8.setText("Registration successful.");
            regNameTF.setText("");
            regUsernameTF.setText("");
            regEmailTF.setText("");
            regPwdTF.setText("");

        }

    }//GEN-LAST:event_regButtonActionPerformed

