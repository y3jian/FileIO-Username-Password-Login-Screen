    private void loginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginButtonActionPerformed
        String loginName = loginNameTF.getText();
        String loginPwd = loginPwdTF.getText();
        boolean match = false;

        if (loginName.length() == 0 || loginPwd.length() == 0) {
            jLabel10.setText("Please fill out all the information above.");
        }
        for (int x = 0; x < usernames.size(); x++) {
            if (loginName.equals(usernames.get(x)) && pwdEncryption(loginPwd).equals(passwords.get(x))) {
                jLabel10.setText("logged in");
                match = true;
                loginNameTF.setText("");
                loginPwdTF.setText("");

            }
        }
        if (!match) {
            jLabel10.setText("Combination not found. Please try again.");
            loginNameTF.setText("");
            loginPwdTF.setText("");

        }
    }//GEN-LAST:event_loginButtonActionPerformed

